#include <wolf.h>

t_murs	*fonction2(char *str, int** textures)
{
	int i;
	int j[8];
	int x;

	i = 0;
	x = 0;
	ft_bzero(j, 32);
	while(x < 8)
	{
		if (str[i] >= '0' && str[i] <= '9')
			j[x] = j[x] * 10 + (str[i] - '0');
		str[i] == '.' ? x++ : 0;
		str[i] == '[' && str[i-1] == ']' ? x++ : 0;
		str[i] == ']' && str[i-1] == ']' ? x++ : 0;
		str[i] == '#' ? x++ : 0;
		i++;
	}
	return(alloc_murs(j, textures));
}
